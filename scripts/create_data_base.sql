-- =====================================================
-- 12. TABLE: customer
-- Business customers for the jewelry store (separate from app_user login)
-- app_user_id FK is added in app_security_users.sql (after app_user exists)
-- =====================================================
CREATE TABLE ALEDIESME.customer (
    id                    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    app_user_id           NUMBER,
    user_type_id          NUMBER        NOT NULL,
    document_type_id      NUMBER        NOT NULL,
    document_number       VARCHAR2(30)  NOT NULL,
    first_name            VARCHAR2(100) NOT NULL,
    last_name             VARCHAR2(100) NOT NULL,
    email                 VARCHAR2(150),
    phone                 VARCHAR2(30),
    mobile_phone          VARCHAR2(30),
    gender_id             NUMBER,
    marital_status_id     NUMBER,
    birth_date            DATE,
    anniversary_date      DATE,
    neighborhood_id       NUMBER,
    address_line          VARCHAR2(200),
    address_complement    VARCHAR2(100),
    postal_code           VARCHAR2(20),
    preferred_currency_id NUMBER,
    accepts_marketing     NUMBER(1)     DEFAULT 0 NOT NULL,
    notes                 VARCHAR2(500),
    enabled               NUMBER(1)     DEFAULT 1 NOT NULL,
    created_at            DATE          DEFAULT SYSDATE NOT NULL,
    updated_at            DATE          DEFAULT SYSDATE NOT NULL,
    CONSTRAINT fk_customer_user_type FOREIGN KEY (user_type_id)
        REFERENCES ALEDIESME.user_type (id),
    CONSTRAINT fk_customer_document_type FOREIGN KEY (document_type_id)
        REFERENCES ALEDIESME.document_type (id),
    CONSTRAINT fk_customer_gender FOREIGN KEY (gender_id)
        REFERENCES ALEDIESME.gender (id),
    CONSTRAINT fk_customer_marital_status FOREIGN KEY (marital_status_id)
        REFERENCES ALEDIESME.marital_status (id),
    CONSTRAINT fk_customer_neighborhood FOREIGN KEY (neighborhood_id)
        REFERENCES ALEDIESME.neighborhood (id),
    CONSTRAINT fk_customer_preferred_currency FOREIGN KEY (preferred_currency_id)
        REFERENCES ALEDIESME.currency (id),
    CONSTRAINT uk_customer_document UNIQUE (document_type_id, document_number),
    CONSTRAINT uk_customer_app_user UNIQUE (app_user_id),
    CONSTRAINT uk_customer_email UNIQUE (email),
    CONSTRAINT ck_customer_enabled CHECK (enabled IN (0, 1)),
    CONSTRAINT ck_customer_accepts_marketing CHECK (accepts_marketing IN (0, 1))
);

