-- 1. TABLE: document_type
-- =====================================================
CREATE TABLE ALEDIESME.document_type (
    id           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code         VARCHAR2(10)  NOT NULL,
    name         VARCHAR2(100) NOT NULL,
    abbreviation VARCHAR2(5),
    enabled      NUMBER(1)     DEFAULT 1 NOT NULL,
    created_at   DATE          DEFAULT SYSDATE NOT NULL,
    updated_at   DATE          DEFAULT SYSDATE NOT NULL,
    CONSTRAINT uk_document_type_code UNIQUE (code),
    CONSTRAINT ck_document_type_enabled CHECK (enabled IN (0, 1))
);

-- =====================================================
-- 2. TABLE: country
-- =====================================================
CREATE TABLE ALEDIESME.country (
    id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    iso_code   VARCHAR2(2)   NOT NULL,
    name       VARCHAR2(100) NOT NULL,
    demonym    VARCHAR2(50),
    enabled    NUMBER(1)     DEFAULT 1 NOT NULL,
    created_at DATE          DEFAULT SYSDATE NOT NULL,
    updated_at DATE          DEFAULT SYSDATE NOT NULL,
    CONSTRAINT uk_country_iso_code UNIQUE (iso_code),
    CONSTRAINT uk_country_name UNIQUE (name),
    CONSTRAINT ck_country_enabled CHECK (enabled IN (0, 1))
);

-- =====================================================
-- 3. TABLE: state_province
-- =====================================================
CREATE TABLE ALEDIESME.state_province (
    id         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    country_id NUMBER        NOT NULL,
    code       VARCHAR2(5)   NOT NULL,
    name       VARCHAR2(100) NOT NULL,
